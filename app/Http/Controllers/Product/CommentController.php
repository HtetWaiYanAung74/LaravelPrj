     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $delete=Comment::find($id);

        if($delete->user_id == auth()->user()->id)
        {
            $delete->delete();
            return back()->with('info','Comment has been successfully deleted...');
        }else{
